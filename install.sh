# Backup if needed
if [ -d "$APPDIR" ]; then
  execute "backupapp $APPDIR $APPNAME" "Backing up $APPDIR"
fi
# Main progam
if am_i_online; then
  if [ -d "$INSTDIR/.git" ]; then
    execute "git_update $INSTDIR" "Updating $APPNAME configurations"
  else
    execute "git_clone $REPO $INSTDIR" "Installing $APPNAME ions"
  fi
  # exit on fail
  failexitcode $? "Failed to download $REPO/$APPNAME to $INSTDIR"
fi
# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
# Plugins
if am_i_online; then
  if [ "$PLUGNAMES" != "" ]; then
    if [ -d "$HOME/.emacs.d/.git" ]; then
      execute "git_update $HOME/.emacs.d" "Updating plugin doom-emacs"
    else
      execute "git_clone https://github.com/hlissner/doom-emacs $HOME/.emacs.d" "Installing plugin doom-emacs"
    fi
  fi
  # exit on fail
  failexitcode $? "Failed to download Plugin repo"
fi
# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
# run post install scripts
run_postinst() {
  dfmgr_run_post
  EMACSVER="$(emacs --version | head -n 1 | awk -F'[^0-9]*' '$0=$2')"
  if [[ "$EMACSVER" -lt "26" ]]; then
    printf_red "Please update emacs" >&2
  else
    if [ -f "$PLUGDIR/bin/doom" ]; then
      "$PLUGDIR/bin/doom" -y env
      "$PLUGDIR/bin/doom" -y install --no-env
      cp_rf "$APPDIR"/*.el "$CONF/doom/"
      "$PLUGDIR/bin/doom" -y sync
      "$PLUGDIR/bin/doom" -y sync
    fi
  fi
}
#
execute "run_postinst" "Running post install scripts"
