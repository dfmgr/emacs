# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
# Call the dfmgr function
dfmgr_install
# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
# Script options IE: --help --version
